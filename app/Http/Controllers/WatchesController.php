     *
     * @param  \Illuminate\Http\Request  $request
     * @return \Illuminate\Http\Response
     */
    public function store(Request $request)
    {
        $data = $request->validate( [
            'name'           => 'required',
            'brand'          => 'required',
            'reference'      => 'required',
            'year'           => [
                'integer',
                'min:1900',
                'max:' . date( 'Y' )
            ],
            //'warranty'       => 'required',
            'featured_image' => [
                'required',
                'image'
            ]
        ] );

        Watch::create( $data );
    }

